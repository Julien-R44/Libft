#include "libft.h"

t_dlist	*ft_dlstati(t_dlist *list, unsigned int i)
{
	t_dlist *nav;

	nav = list;
	while (nav->next && (i - 1))
	{
		i--;
		nav = nav->next;
	}
	return (nav);
}
